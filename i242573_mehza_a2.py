class GameState:
    def __init__(self, hands, top, deck, discard, current=0):
        self.hands = hands
        self.top = top
        self.deck = deck
        self.discard = discard
        self.current = current

    def clone(self):
        return GameState(
            {p: list(h) for p, h in self.hands.items()},
            self.top,
            list(self.deck),
            list(self.discard),
            self.current
        )

    def is_terminal(self):
        return any(len(h) == 0 for h in self.hands.values())

    def winner(self):
        for p, h in self.hands.items():
            if len(h) == 0:
                return p
        return None

    def next_player(self, skip=False):
        nxt = (self.current + 1) % 3
        if skip:
            nxt = (nxt + 1) % 3
        return nxt


def apply_move(state, move):
    state = state.clone()
    p = state.current

    if move == "draw":
        card = draw_card(state.deck, state.discard)
        state.hands[p].append(card)
        state.current = state.next_player()
        return state

    state.hands[p].remove(move)
    state.discard.append(move)
    state.top = move

    if move.is_skip():
        state.current = state.next_player(skip=True)
    else:
        state.current = state.next_player()

    return state


def evaluate(state, player_id, mode="defensive"):
  my_cards = len(state.hands[player_id]) #no of cards we have

  opp_cards = []
  for pid, h in state.hands.items():
      if pid != player_id:
          opp_cards.append(len(h))

  total = sum(opp_cards)
  count = len(opp_cards)
  avg_opp = total / count

  skips = 0
  for c in state.hands[player_id]:
      if c.is_skip():
          skips += 1

  if mode == "defensive":
      return 50 - 7*my_cards + 1.5*avg_opp + 4*skips
  elif mode == "offensive":
      return 50 - 4*my_cards + 3*avg_opp + 2*skips
  else:
      return 50 - 5*my_cards + 2*avg_opp + 3*skips


class MinimaxAgent:
    def __init__(self, pid, depth=3):
        self.pid = pid
        self.depth = depth

    def choose_move(self, state):
        moves = get_valid_moves(state.hands[self.pid], state.top) or ["draw"]
        best_move = moves[0]
        best_score = float("-inf")

        print("\nMinimax evaluating:")

        for m in moves:
            score = self.minimax(apply_move(state, m), self.depth-1)
            print(" ", m, "->", round(score,2))
            if score > best_score:
                best_score = score
                best_move = m

        return best_move

    def minimax(self, state, depth):
        if state.is_terminal() or depth == 0:
            return evaluate(state, self.pid, "defensive")

        current = state.current
        moves = get_valid_moves(state.hands[current], state.top) or ["draw"]

        if current == self.pid:
            return max(self.minimax(apply_move(state, m), depth-1) for m in moves)
        else:
            return min(self.minimax(apply_move(state, m), depth-1) for m in moves)



class ExpectimaxAgent:
    def __init__(self, pid, depth=3):
        self.pid = pid
        self.depth = depth

    def choose_move(self, state):
        moves = get_valid_moves(state.hands[self.pid], state.top) or ["draw"]
        best_move = moves[0]
        best_score = float("-inf")

        print("\nExpectimax evaluating:")

        for m in moves:
            if m == "draw":
                score = self.chance_node(state, self.depth-1)
            else:
                score = self.expectimax(apply_move(state, m), self.depth-1)

            print(" ", m, "->", round(score,2))

            if score > best_score:
                best_score = score
                best_move = m

        return best_move

    def expectimax(self, state, depth):
        if state.is_terminal() or depth == 0:
            return evaluate(state, self.pid, "offensive")

        current = state.current
        moves = get_valid_moves(state.hands[current], state.top) or ["draw"]

        if current == self.pid:
            return max(self.expectimax(apply_move(state, m), depth-1) for m in moves)
        else:
            return sum(self.expectimax(apply_move(state, m), depth-1) for m in moves)/len(moves)

    def chance_node(self, state, depth):
        if not state.deck:
            return evaluate(state, self.pid, "offensive")

        total = 0
        for card in state.deck:
            sim = state.clone()
            sim.hands[sim.current].append(card)
            sim.current = sim.next_player()
            total += self.expectimax(sim, depth)

        return total / len(state.deck)


def setup_game():
    deck = build_deck()
    hands = {0: [], 1: [], 2: []}

    for _ in range(5):
        for p in hands:
            hands[p].append(deck.pop())

    while True:
        top = deck.pop()
        if not top.is_skip():
            break

    return GameState(hands, top, deck, [top])

def run_game():
    state = setup_game()

    agents = {
        0: MinimaxAgent(0),
        1: ExpectimaxAgent(1),
        2: MinimaxAgent(2)
    }

    turn = 1

    while not state.is_terminal() and turn <= 200:
        print("\n-------------------------")
        print("Turn", turn)
        print("Top card:", state.top)

        p = state.current
        print("Current:", PLAYER_NAMES[p])

        move = agents[p].choose_move(state)

        if move == "draw":
            print(" -> draws a card")
        else:
            print(" -> plays", move)

        state = apply_move(state, move)
        turn += 1

    print("\nWinner:", PLAYER_NAMES[state.winner()])


